export interface ServiceSchemaProps {
  name: string
  description: string
  url: string
  serviceType: ServiceType
  serviceOutput: ServiceOutput
  faqs: Array<{
    question: string
    answer: string
  }>
}

export function getServiceSchema({ name, description, url, serviceType, serviceOutput, faqs }: ServiceSchemaProps) {
  // Проверяем, что faqs - это массив
  const safeFaqs = Array.isArray(faqs) ? faqs : []

  return {
    "@context": "https://schema.org",
    "@type": serviceType,
    name: name,
    description: description,
    url: url,
    serviceType: serviceOutput,
    hasOfferCatalog: {
      "@type": "OfferCatalog",
      name: "Юридическая помощь",
      itemListElement: [
        {
          "@type": serviceType,
          name: name,
        },
      ],
    },
    ...(safeFaqs.length > 0
      ? {
          mainEntity: safeFaqs.map((faq) => ({
            "@type": "Question",
            name: faq.question || "",
            acceptedAnswer: {
              "@type": "Answer",
              text: faq.answer || "",
            },
          })),
        }
      : {}),
  }
}
